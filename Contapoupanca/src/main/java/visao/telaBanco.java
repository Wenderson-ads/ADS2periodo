    private void jButton1criarContaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1criarContaActionPerformed
        // TODO add your handling code here:

        try {
            conta = new contaPoupanca();
            contaPoupanca cp = new contaPoupanca();
            String nome = jTextField1nomeCliente.getText();
            int numeroConta = Integer.parseInt(jTextField2numeroConta.getText());
            int agencia = Integer.parseInt(jTextField2numeroConta.getText());
            conta.setNomeCliente(nome);
            conta.setNumeroConta(numeroConta);
            conta.setAgencia(agencia);
            jTextField4saldo.setText("R$00.00");
            jButton1criarConta.setEnabled(false);
            jTextField4valor.setEnabled(true);
            jButton2Ssacar.setEnabled(true);
            jButton1depositar.setEnabled(true);

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro.getMessage());
        }

    }//GEN-LAST:event_jButton1criarContaActionPerformed

    private void jButton2SsacarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2SsacarActionPerformed
        try {
            int valor = Integer.parseInt(jTextField4valor.getText());
            conta.sacar(valor);
            String valorSaldo = "R$" + conta.getSaldo();
            jTextField4saldo.setText(valorSaldo);

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(this, erro);
        }

    }//GEN-LAST:event_jButton2SsacarActionPerformed

