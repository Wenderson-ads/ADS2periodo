            saida += "VALOR HORAS :" + co.getValorHorasTrabalhadas() + "\n";
            saida += "BONIFICA√á√ÉO :" + co.bonificacao() + "\n";
            saida += "SAL√ÅRIO LIQUIDO :" + co.salarioLiquido() + "\n";
            saida += "CONTRIBUI√á√ÉO :" + co.contribuicao() + "\n";
            saida += "SAL√ÅRIO FINAL:" + co.salarioFinal() + "\n";
            
            jTextArea1saida.setText(saida);

